package condition;

public class Condition2 {
    public static void main(String[] args) {
        int a = 1;

        switch (a) {
            case 1:
                System.out.println("C");
            case 2:
                System.out.println("R");
            case 3:
                System.out.println("U");
            case 4:
                System.out.println("D");
                break;
            default:
                break;
        }
    }
}
